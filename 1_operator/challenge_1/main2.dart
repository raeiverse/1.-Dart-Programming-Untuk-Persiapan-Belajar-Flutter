void main() {
  int x = 2;
  int y = 4;
  int z = 6;
  int kel_persegi = 4 * x;
  double luas_lingkaran = 3.14 * z * z;
  int vol_balok = x * y * z;
  print('Keliling Persegi = ' + kel_persegi.toString());
  print('Luas Lingkaran = ' + luas_lingkaran.toStringAsFixed(2));
  print('Volume Balok = ' + vol_balok.toString());
}
