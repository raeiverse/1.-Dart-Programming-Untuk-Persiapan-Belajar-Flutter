void main() {
  var a = 12;
  var b = 10;
  var c = a + b;
  //aritmatik operator / perhitungan
  var penjumlahan = a + b;
  var pengurangan = b - a;
  var perkalian = a * b;
  var pembagian = a / b;
  var sisa = a % b;
  print(penjumlahan);
  print(pengurangan);
  print(perkalian);
  print(pembagian);
  print(sisa);
  // equality & relational operator
  print(a > b); // kurang dari
  print(a < b); // lebih dari
  print(a == b); // sama dengan
  print(a != b); // tidak sama dengan
  print(a >= b); // lebih dari sama dengan
  print(a <= b); // kurang dari sama dengan
  //logika operator
  bool x = true;
  bool y = true;
  bool z = false;
  print(x && y); //&& operasi And
  print(x || z);
  print(!z); //! operator Not
}
